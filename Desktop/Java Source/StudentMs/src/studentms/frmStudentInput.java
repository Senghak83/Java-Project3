    private void btupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btupdateActionPerformed
        
        int y;
        int rs = t_student.getSelectedRow();
        
        if(rs!=-1 && AddData()!=null){                 
            y = JOptionPane.showConfirmDialog(null,"Do you want to update "
                    + "\n This record","Update Record",JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
                if(y==JOptionPane.YES_OPTION){
                    db.Update(AddData(), rs);
                    ShowData(db.getAllStudents(),t_student);
                    this.btnewActionPerformed(evt);
                
            }
            
        }// end if
    }//GEN-LAST:event_btupdateActionPerformed

