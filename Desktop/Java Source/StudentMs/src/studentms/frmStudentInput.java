    private void btdeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btdeletActionPerformed
        //  get messagge
        int rs = t_student.getSelectedRow();
        if(rs!=-1){
            
            int y = JOptionPane.showConfirmDialog(null, "Do you want to remove \n This record",
                "Remove Record",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(y==JOptionPane.YES_OPTION){
              db.Delete(rs);
              ShowData(db.getAllStudents(),t_student);
              btdelet.setEnabled(false);
              btupdate.setEnabled(false);
              
              this.btnewActionPerformed(evt);
            }
        }
        
    }//GEN-LAST:event_btdeletActionPerformed

    private void t_studentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_studentMouseClicked
        // TODO add your handling code here:
        int rs = t_student.getSelectedRow();
        if(rs!=-1){
            btupdate.setEnabled(true);
            btdelet.setEnabled(true);
            btadd.setEnabled(false);
            btnew.setEnabled(true);
            
            Students s = db.get(rs);
            txtid.setText(s.getId());
            txtname.setText(s.getName());
            if(t_student.getValueAt(rs, 3).toString().equals("Male")){
                cbosex.setSelectedIndex(0);
            }
            else
                cbosex.setSelectedIndex(1);
            txtdob.setText(s.getDob());
            //--- for jcobo
            for(int i = 0 ; i<cbodepart.getItemCount()-1; i++){
                if(t_student.getValueAt(rs, 5).equals(cbodepart.getItemAt(i))){
                    cbodepart.setSelectedIndex(i);
                    break;
                }
            }// end loop
            txtmajor.setText(s.getMajor());
            txtyear.setText(s.getYear()+"");
            
        }// check rs!=-1
    }//GEN-LAST:event_t_studentMouseClicked

