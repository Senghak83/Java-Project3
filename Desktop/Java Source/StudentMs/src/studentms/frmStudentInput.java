    private Students AddData(){
        //-- get data from Input form
        String id = txtid.getText();
        String n = txtname.getText();
        String s = cbosex.getSelectedItem().toString();
        String dob = txtdob.getText();
        String dp = cbodepart.getSelectedItem().toString();
        String maj = txtmajor.getText();
        String y = txtyear.getText();
        
        // check validation
        if(id.length()==0)Msg("Input ID",txtid);
        else if(n.length()==0)Msg("Input Name",txtname);
        else if(dob.length()==0)Msg("Input Dob",txtdob);
        else if(y.length()==0)Msg("Inpuyt year ",txtyear);
        else if(maj.length()==0)Msg("Input Major",txtmajor);
        else{
            try{
                int year = Integer.parseInt(y);
                return new Students(new Person(id,n,s,dob),dp,maj,year);
            }catch(NumberFormatException exp){
                Msg("Year only number",txtyear);
                txtyear.selectAll();
                return null;
            }
        }
        
        return null;
    }
    //   Method display Data in JTable
    
     void ShowData(ArrayList<Students> ls, JTable t){
         int n = 1;
         // remove data from table
         int rc = t.getRowCount();
         for(int i = rc -1 ; i>=0 ; i--)
             model.removeRow(i);
         
         for(Students s : ls){
             model.addRow(new Object[]{n,s.getId(),s.getName(),s.getSex(),
             s.getDob(),s.getDepart(),s.getMajor(),s.getYear()});
             n++;
         }
         t.setModel(model);
    }
    
